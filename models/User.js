import mongoose from 'mongoose';

const Userschema = mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    role: {
        type: String,
        default: "user"
    }
}, { timestamps: true })

export const User = new mongoose.model("users", Userschema)